// Utility to clean up price string and get the number and currency
const parsePrice = (priceString) => {
    let price = 0;
    let currency = "â‚¹";
    const match = priceString.match(/([â‚¹$])?([\d,.]+)/);
    if (match) {
        currency = match[1] || 'â‚¹';
        price = parseFloat(match[2].replace(/,/g, ''));
    }
    return { price, currency };
};

